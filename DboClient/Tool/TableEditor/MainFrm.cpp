LRESULT CMainFrame::OnUpdateIcon(WPARAM, LPARAM lParam)
{
	CString* pIcon = reinterpret_cast<CString*>(lParam);
	if (pIcon)
	{
		m_wndItemPreview.SetIconImage(*pIcon);
		delete pIcon;
	}
	return 0;
}

